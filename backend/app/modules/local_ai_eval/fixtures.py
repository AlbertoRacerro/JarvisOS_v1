from typing import Any


def _ctx(
    *,
    current_task: str = "",
    conversation_summary: str = "",
    project_state: str = "",
    relevant_decisions: list[str] | None = None,
    tool_results: list[dict[str, Any]] | None = None,
    known_constraints: list[str] | None = None,
    forbidden_actions: list[str] | None = None,
) -> dict[str, Any]:
    return {
        "current_task": current_task,
        "conversation_summary": conversation_summary,
        "project_state": project_state,
        "relevant_decisions": relevant_decisions or [],
        "tool_results": tool_results or [],
        "known_constraints": known_constraints or [],
        "forbidden_actions": forbidden_actions or [],
    }


def _expected(
    task_type: str,
    sensitivity: str,
    complexity: str,
    selected_local_action: str,
    *,
    must_include: list[str] | None = None,
    must_not_include: list[str] | None = None,
    expected_decisions: list[str] | None = None,
    expected_todos: list[str] | None = None,
    expected_missing_context_flags: list[str] | None = None,
    expected_state: str = "READY_LOCAL_RESPONSE",
    expected_requested_context_packages: list[str] | None = None,
    forbidden_context_packages: list[str] | None = None,
    context_sufficiency: str = "not_applicable",
    expected_allowed_tool_requests: list[str] | None = None,
    forbidden_tool_requests: list[str] | None = None,
    external_call_requested: bool = False,
    external_call_allowed_by_model: bool = False,
) -> dict[str, Any]:
    return {
        "task_type": task_type,
        "sensitivity": sensitivity,
        "complexity": complexity,
        "selected_local_action": selected_local_action,
        "must_include": must_include or [],
        "must_not_include": must_not_include or [],
        "expected_decisions": expected_decisions or [],
        "expected_todos": expected_todos or [],
        "expected_missing_context_flags": expected_missing_context_flags or [],
        "expected_state": expected_state,
        "expected_requested_context_packages": expected_requested_context_packages or [],
        "forbidden_context_packages": forbidden_context_packages or [],
        "context_sufficiency": context_sufficiency,
        "expected_allowed_tool_requests": expected_allowed_tool_requests or [],
        "forbidden_tool_requests": forbidden_tool_requests or [],
        "external_call_requested": external_call_requested,
        "external_call_allowed_by_model": external_call_allowed_by_model,
    }


def _case(
    case_id: str,
    category: str,
    text: str,
    context: dict[str, Any],
    expected: dict[str, Any],
    severity: str,
    notes: str,
) -> dict[str, Any]:
    return {
        "id": case_id,
        "category": category,
        "input": text,
        "provided_context": context,
        "expected": expected,
        "severity": severity,
        "notes": notes,
    }


